def main() -> None:
    """Generate centroid vectors from prototype prompts."""
    try:
        from sentence_transformers import SentenceTransformer
    except ImportError:
        print(
            "ERROR: sentence-transformers is required. "
            "Install with: pip install sentence-transformers",
            file=sys.stderr,
        )
        sys.exit(1)

    project_root = Path(__file__).resolve().parent.parent
    output_dir = project_root / "models" / "centroids"
    output_dir.mkdir(parents=True, exist_ok=True)

    model_name = "sentence-transformers/all-MiniLM-L6-v2"
    print(f"Loading encoder: {model_name}")
    encoder = SentenceTransformer(model_name, device="cpu")

    # Encode prototypes
    print(f"Encoding {len(SIMPLE_PROTOTYPES)} simple prototypes...")
    simple_embeddings = encoder.encode(SIMPLE_PROTOTYPES, show_progress_bar=True)

    print(f"Encoding {len(COMPLEX_PROTOTYPES)} complex prototypes...")
    complex_embeddings = encoder.encode(COMPLEX_PROTOTYPES, show_progress_bar=True)

    # Compute mean centroids
    simple_centroid = np.mean(simple_embeddings, axis=0)
    complex_centroid = np.mean(complex_embeddings, axis=0)

    # L2-normalize to unit vectors
    simple_centroid = simple_centroid / np.linalg.norm(simple_centroid)
    complex_centroid = complex_centroid / np.linalg.norm(complex_centroid)

    # Verify
    print(
        f"\nSimple centroid: shape={simple_centroid.shape}, norm={np.linalg.norm(simple_centroid):.6f}"
    )
    print(
        f"Complex centroid: shape={complex_centroid.shape}, norm={np.linalg.norm(complex_centroid):.6f}"
    )

    # Cosine similarity between centroids (should be moderate, not too high)
    inter_sim = float(np.dot(simple_centroid, complex_centroid))
    print(f"Inter-centroid cosine similarity: {inter_sim:.6f}")

    # Save
    simple_path = output_dir / "simple_centroid.npy"
    complex_path = output_dir / "complex_centroid.npy"

    np.save(str(simple_path), simple_centroid)
    np.save(str(complex_path), complex_centroid)

    print(f"\n✅ Centroid vectors saved to {output_dir}")
    print(f"  {simple_path.name}: {os.path.getsize(simple_path)} bytes")
    print(f"  {complex_path.name}: {os.path.getsize(complex_path)} bytes")


if __name__ == "__main__":
    main()
